def resolve_stock_code(company: str) -> tuple[str, str]:
    """(6자리 종목코드 또는 빈 문자열, 비고) 반환. 확인 불가 사유가 있으면 비고에 담는다.
    STOCK_CODE_MAP은 원래 이름으로 먼저 찾고, 없을 때만 별칭(KRX 등록명 등)으로 재시도한다."""
    if company in UNRESOLVED_STOCK_NOTES:
        return "", UNRESOLVED_STOCK_NOTES[company]

    raw_code = STOCK_CODE_MAP.get(company)
    if raw_code is None:
        alias = STOCK_NAME_ALIASES.get(company)
        raw_code = STOCK_CODE_MAP.get(alias) if alias else None
    if raw_code is None:
        return "", "종목코드 미확인 - 수동 확인 필요"

    code = raw_code.strip()
    if not code.isdigit() or len(code.zfill(6)) != 6:
        return "", f"종목코드 형식 이상: {raw_code}"
    return code.zfill(6), ""
